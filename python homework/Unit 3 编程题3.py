'''
3.有如下字典 dic={"a1":"11","a2":"22","a3":[11,22,33] ,"a4":"44"}
(1)输出字典中所有的键及所有的值。
(2)在字典中添加键值对"a4"："55"后输出整个字典的键值对。
(3)修改键a2的值为88。
'''
# 创建字典
dic = {"a1":"11", "a2":"22", "a3":[11,22,33], "a4":"44"}

# (1) 输出所有键和值
print("(1) 输出所有键和值：")
print("所有的键：", list(dic.keys()))
print("所有的值：", list(dic.values()))

# (2) 添加新键值对并输出
dic["a4"] = "55"  # 这将覆盖原有的 "a4" 键值对
print("\n(2) 添加新键值对后的字典：")
for key, value in dic.items():
    print(f"{key}: {value}")

# (3) 修改 a2 的值
dic["a2"] = "88"
print("\n(3) 修改 a2 的值后的字典：")
for key, value in dic.items():
    print(f"{key}: {value}")